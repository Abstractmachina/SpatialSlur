namespace SpatialSlur.SlurDynamics
{
    /// <summary>
    /// Base class for constraints.
    /// </summary>
    [Serializable]
    public abstract class Constraint
    {
        private double _weight;


        /// <inheritdoc/>
        /// <summary>
        /// 
        /// </summary>
        public double Weight
        {
            get { return _weight; }
            set
            {
                if (value < 0.0)
                    throw new ArgumentOutOfRangeException("Weight cannot be negative.");

                _weight = value;
            }
        }
    }
}
